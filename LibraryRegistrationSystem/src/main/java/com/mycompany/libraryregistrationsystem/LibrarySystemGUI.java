    private void addBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookButtonActionPerformed
        // TODO add your handling code here:
        try {
        String bookTitle = bookTitleTextField.getText().trim();
        String bookGenre = bookGenreTextField.getText().trim();
        String bookVersion = bookVersionTextField.getText().trim();
        
        if (bookTitle.isEmpty() || bookGenre.isEmpty() || bookVersion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all book fields.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int day = bookDayComboBox.getSelectedIndex() + 1;
        int month = bookMonthComboBox.getSelectedIndex() + 1;
        int year = Integer.parseInt(bookYearTextField.getText());
        
        
        Date publicationDate = createDate(day, month, year);
        if (publicationDate == null) {
            JOptionPane.showMessageDialog(this, "Invalid publication date.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String authorName = BookAuthorNameTextField.getText().trim();
        if (authorName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the author's name.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Author author;
        if ((author = library.searchAuthor(authorName)) == null) {
            String address = authorAddressTextField.getText().trim();
            int aDay = authorDayComboBox.getSelectedIndex() + 1;
            int aMonth = authorMonthComboBox.getSelectedIndex() + 1;
            int aYear = Integer.parseInt(authorYearTextField.getText());
            Date birthDate = createDate(aDay, aMonth, aYear);

            if (birthDate == null || address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Invalid author details.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            author = new Author(authorName, address, birthDate);
        }
        
        Books book = new Books(bookTitle, author, bookGenre, bookVersion, publicationDate);
        library.addBook(book);
        JOptionPane.showMessageDialog(this, "Book added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid number format.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}


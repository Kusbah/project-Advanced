    private void bookSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookSearchButtonActionPerformed
        // TODO add your handling code here:
        String bookTitle = bookTitleTextFieldForBookSearch.getText().trim();
    String bookNoText = bookNumberTextFieldForBookSearch.getText().trim();
    String authorName = bookAuthorTextFieldForBookSearch.getText().trim();

    List<Books> searchResults = new ArrayList<>();

    try {
        if (!bookTitle.isEmpty()) {
            searchResults.addAll(library.searchByTitle(bookTitle));
        }

        if (!bookNoText.isEmpty()) {
            int bookNo = Integer.parseInt(bookNoText);
            Books book = library.searchByNumber(bookNo);
            if (book != null) {
                searchResults.add(book);
            }
        }

        if (!authorName.isEmpty()) {
            searchResults.addAll(library.searchByAuthorName(authorName));
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid book number format: " + bookNoText, "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    
    searchResults = new ArrayList<>(new HashSet<>(searchResults));

    if (searchResults.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No books found matching the criteria.", "Information", JOptionPane.INFORMATION_MESSAGE);
    } else {
        updateSearchResultsList(searchResults);
    }
}

private void updateSearchResultsList(List<Books> books) {
    DefaultListModel<String> model = new DefaultListModel<>();
    for (Books book : books) {
        model.addElement(book.getInfo()); 
    }
    searchResultList.setModel(model);
    }//GEN-LAST:event_bookSearchButtonActionPerformed

    private void nookLoanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nookLoanButtonActionPerformed
        // TODO add your handling code here:                                              
    try {
        String bookTitle = bookTitleTextFieldForBookLoan.getText().trim();
        int studentId = Integer.parseInt(jTextField16.getText().trim());

        Books book = library.searchByTitle(bookTitle).isEmpty() ? null : library.searchByTitle(bookTitle).get(0);
        Students student = getStudentById(studentId);

        if (book != null && student != null) {
            java.time.LocalDate dueDate = java.time.LocalDate.now().plusDays(5);

            Loan loan = new Loan(student, book, dueDate);
            library.addLoan(loan);
            JOptionPane.showMessageDialog(this, "Loan created successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Book or Student not found.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid Student ID. Please enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

}


private Students getStudentById(int studentId) {
    
    for (Students student : library.getStudents()) {
        if (student.getID() == studentId) { 
            return student;
        }
    }
    return null;
    }//GEN-LAST:event_nookLoanButtonActionPerformed

    private void returnButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnButtonActionPerformed
        // TODO add your handling code here:
        try {
        String bookTitle = bookTitleTextFieldForBookReturn.getText().trim();
        String bookNumberText = bookNumberTextFieldForBookReturn.getText().trim();
        Books book = null;

        if (!bookTitle.isEmpty()) {
            
            List<Books> foundBooks = library.searchByTitle(bookTitle);
            if (!foundBooks.isEmpty()) {
                book = foundBooks.get(0); 
            }
        } else if (!bookNumberText.isEmpty()) {
            
            int bookNumber = Integer.parseInt(bookNumberText);
            book = library.searchByNumber(bookNumber);
        }

        if (book != null) {
            
            if (returnBook(book)) {
                JOptionPane.showMessageDialog(this, "Book returned successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Error: Book was not on loan.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Book not found.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid book number format.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage());
    }
}

private boolean returnBook(Books book) {
    
    for (Loan loan : library.getLoans()) {
        if (loan.getBook().equals(book) && !loan.isReturned()) {
            loan.setReturned(true); 
            return true;
        }
    }
    return false;
    }//GEN-LAST:event_returnButtonActionPerformed

